

package switchcase;


public class SwitchCase {

    
    public static void main(String[] args) {
        char x='V';
        switch(x)
        {
            case 'a':
            {
                System.out.println("opcion 0");
                break;
            }
            case 'b':
            {
                System.out.println("opcion 1");
                break;
            }
            default:
                System.out.println("opcion incorrecta");
        }
        }
    }
    

