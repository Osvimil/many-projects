

package arraylist;


class persona {
    
    private final String nombre;
    private int edad;
    
    public persona (String nombre, int edad)
    {
        this.nombre=nombre;
        this.edad=edad;
        
    }
    public String getnombre()
    {
        return nombre;
    }
    public int getedad()
    {
        return edad;
        
    }
    
}
